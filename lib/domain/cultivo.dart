class Cultivo {
  final int id;
  final String nombre;
  final String tipo;
  final String fechaSiembra;
  final String ubicacion;
  final double area;

  Cultivo({
    required this.id,
    required this.nombre,
    required this.tipo,
    required this.fechaSiembra,
    required this.ubicacion,
    required this.area,
  });
}
